    private void BTGerarAnaisDoSeminarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTGerarAnaisDoSeminarioActionPerformed
        int mod, unidade, area;
        if (CBModalidade.getSelectedIndex() == 0) {
            mod = -1;
        } else {
            mod = modalidades.get(CBModalidade.getSelectedIndex() - 1).getId();
        }

        if (CBUnidade.getSelectedIndex() == 0) {
            unidade = -1;
        } else {
            unidade = unidades.get(CBUnidade.getSelectedIndex() - 1).getId();
        }
        if (CBArea.getSelectedIndex() == 0) {
            area = -1;
        } else {
            area = areas.get(CBArea.getSelectedIndex() - 1).getId();
        }

        apresentacoes = BancoDeDados.comando().recApresentacoesParaGerarAnaisDoSeminario(eventos.get(CBEvento.getSelectedIndex()).getId(), mod, unidade,
                orders.get(CBOrder.getSelectedIndex()), area);

        int initialC = apresentacoes.size();
        System.out.println("Iniciado retirada de trabalhos que não estão ok dos anais...");
        ArrayList<apresentacao> aps2 = new ArrayList<>();
        for (int a = 0; a < apresentacoes.size(); a++) {
            if (BancoDeDados.comando().trabalhoOkParaAnaisCertificado(apresentacoes.get(a), eventos.get(CBEvento.getSelectedIndex()))) {
                aps2.add(apresentacoes.get(a));
            }
        }
        apresentacoes = aps2;
        System.out.println(aps2.size()+"/"+initialC+" Apresentações podem entrar nos anais devido a avaliações suficientes.");

        GeradorPDF.comando().setPrefixoLink(TFPrefixoLink.getText());
        GeradorPDF.comando().setIncluirSubarea(CHIncluirSubarea.isSelected());

        //if(GeradorPDF.comando().gerarODT(apresentacoes,TFNome.getText(),(int)SPPaginasCapa.getValue()))
        if (GeradorPDF.comando().gerarArquivos(apresentacoes, TFNome.getText(), (int) SPPaginasCapa.getValue(), eventos.get(CBEvento.getSelectedIndex()))) {
            LAviso.setText("Arquivo salvo com sucesso!");
        } else {
            LAviso.setText("Falha ao salvar o arquivo!");
        }


    }//GEN-LAST:event_BTGerarAnaisDoSeminarioActionPerformed

