    private void BTGerarCertificadosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTGerarCertificadosActionPerformed
        ArrayList<apresentacao> apresentacoes;

        GeradorPDF.comando().setPrefixoLink(TFPrefixoLink.getText());
        GeradorPDF.comando().setIncluirSubarea(CHIncluirSubarea.isSelected());

        for (area a : areas) {
            apresentacoes = BancoDeDados.comando().recApresentacoesParaGerarAnaisDoSeminario(eventos.get(CBEvento.getSelectedIndex()).getId(), -1, -1,
                    orders.get(CBOrder.getSelectedIndex()), a.getId());

            //if(GeradorPDF.comando().gerarODT(apresentacoes,TFNome.getText(),(int)SPPaginasCapa.getValue()))
            if (GeradorPDF.comando().gerarArquivos(apresentacoes, TFNome.getText() + "_" + a.getNome(), (int) SPPaginasCapa.getValue(), eventos.get(CBEvento.getSelectedIndex()))) {
                LAviso.setText("Arquivo salvo com sucesso!");
            } else {
                LAviso.setText("Falha ao salvar o arquivo!");
            }
        }

    }//GEN-LAST:event_BTGerarCertificadosActionPerformed

